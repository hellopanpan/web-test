// 正则表达式统一集合，规定项目中所有需要验证的表达式

const regex = {
  name: /^[\u4e00-\u9fa5|\s]+$/, // 姓名
  ID: /(^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$)|(^[1-9]\d{5}\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{2}[0-9Xx]$)/, // 身份证
  phone: /^1[^012][0-9]\d{8}$/, // 手机号
  bankCard: /^\d{16,19}$/, // 银行卡号
  smsCode: /^\d{6}$/, // 短信验证码
  email: '',
  contactName: /^[\u4e00-\u9fa5|\s|a-z|A-Z]+$/
}

export default regex
